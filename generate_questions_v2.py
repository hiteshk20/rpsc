# Helper to expand a bank to N by cycling with option shuffle
def expand_bank(bank, n):
    out=[]
    for i in range(n):
        q,o,a,topic,exp = bank[i % len(bank)]
        opts = o[:]
        # shuffle distractors keeping correct tracked
        correct_text = opts[a]
        random.shuffle(opts)
        new_a = opts.index(correct_text)
        out.append({"q":q, "options":opts, "answer":new_a, "explanation":exp, "topic":topic})
    return out

