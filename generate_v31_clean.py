def mq(q, options, ans, topic, explanation="", difficulty=None):
    options = [str(o).strip() or "कोई नहीं" for o in options]
    while len(options) < 4: options.append("कोई नहीं")
    options = options[:4]
    ans = ans if 0 <= ans < 4 else 0
    if not difficulty:
        difficulty = random.choices(['Easy','Medium','Hard'], weights=[30,50,20])[0]
    return {"q":q, "options":options, "answer":ans, "topic":topic, "explanation":explanation or "RPSC 2nd Grade PYQ Pattern", "difficulty":difficulty}

