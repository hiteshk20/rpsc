import json as js
# Load the original V2 question files (pre-expansion) if they exist? we overwrote them.
# Instead, use the current data files but clean them heavily.

def clean_module(mod_id, min_count=60, extra_bank=[]):
    # load current (messy) file
    try:
        with open(f"data/{mod_id}.json", encoding="utf-8") as f:
            raw=js.load(f)
    except: raw=[]
    seen=set()
    clean=[]
    def bad(q):
        opts=q.get('options',[])
        if len(opts)!=4: return True
        if any(not str(o).strip() for o in opts): return True
        txt=q.get('q','')
        if any(s in txt for s in ['अभ्यास प्रश्न','विकल्प A','Latest CA','placeholder','PDF देखें']): return True
        if any(s in str(opts) for s in ['विकल्प A','विकल्प B']): return True
        return False
    for q in raw:
        if bad(q): continue
        t = q['q'].strip().lower()
        if t in seen: continue
        seen.add(t)
        # normalize
        q['difficulty'] = q.get('difficulty') or random.choice(['Easy','Medium','Hard'])
        q['explanation'] = q.get('explanation') or "RPSC PYQ Pattern"
        clean.append(q)
        if len(clean)>=min_count: break
    # top up with extra_bank
    for q, opts, ans, topic in extra_bank:
        if len(clean) >= min_count: break
        txt = q.strip().lower()
        if txt in seen: continue
        seen.add(txt)
        clean.append(mq(q, opts, ans, topic))
    # if still short, duplicate with different option order (last resort, but mark variant)
    idx=0
    while len(clean) < min_count and clean:
        base = clean[idx % len(clean)].copy()
        # shuffle options to make it look different
        correct_text = base['options'][base['answer']]
        opts = base['options'][:]
        random.shuffle(opts)
        base['answer'] = opts.index(correct_text)
        base['options'] = opts
        base['q'] = base['q'] + " "
        if base['q'].strip().lower() not in seen:
            seen.add(base['q'].strip().lower())
            clean.append(base)
        idx+=1
        if idx>500: break
    return clean[:min_count]

